package consoleToth.model;

public class Adiccoes {

	private int id;
	private int pacienteId;
	private int adiccaoId;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPacienteId() {
		return pacienteId;
	}
	public void setPacienteId(int pacienteId) {
		this.pacienteId = pacienteId;
	}
	public int getAdiccaoId() {
		return adiccaoId;
	}
	public void setAdiccaoId(int adiccaoId) {
		this.adiccaoId = adiccaoId;
	}
	
}
